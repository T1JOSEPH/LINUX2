uname -a #muestra toda la información del sistema, como el tipo de kernel, la versión y el nombre del equipo.
lscpu #muestra la información del procesador, como núcleos, arquitectura y velocidad.
free -h #muestra cuánta memoria RAM está en uso y cuánta está libre, en un formato fácil de leer.
df -h #muestra el espacio disponible y usado en los discos, en un formato fácil de entender.